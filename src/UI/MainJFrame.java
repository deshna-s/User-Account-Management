    private void loginbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginbtnActionPerformed
        var username = usernametxt.getText();
        var hashedPassword = ul.HashPassword(new String(passwordtxt.getPassword()));
        var userCred = ul.getUserByUsernamePass(username, hashedPassword);

        if(userCred != null && userCred.isIsAdmin() && logintxt.getSelectedItem().toString().equals("Admin")){
            AdminMainPanel adminMainPanel = new AdminMainPanel(ul);
            jSplitPane1.setRightComponent(adminMainPanel);
            btnLogout.setVisible(true);
        }
        else if(userCred != null && userCred.isIsAdmin() == false && logintxt.getSelectedItem().toString().equals("User")) {
            if(userCred.getIsIsEnabled()) {
                UserUpdatePanel userManagerPanel = new UserUpdatePanel(userCred, ul);
                jSplitPane1.setRightComponent(userManagerPanel);
                btnLogout.setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(this, "User has been disabled by Admin");
            }

        }
        else{
            JOptionPane.showMessageDialog(this, "Password and Username is incorrect");
        }

    }//GEN-LAST:event_loginbtnActionPerformed

